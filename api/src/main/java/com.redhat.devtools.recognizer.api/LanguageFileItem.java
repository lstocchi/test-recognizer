package com.redhat.devtools.recognizer.api;

public class LanguageFileItem {
    private String name;
    private String type;
    private String group;

    public LanguageFileItem(String name, String type, String group) {
        this.name = name;
        this.type = type;
        this.group = group;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getGroup() {
        return group;
    }

    public String getType() {
        return type;
    }
}
